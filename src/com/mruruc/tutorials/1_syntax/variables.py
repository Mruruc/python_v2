# variables are named piece of the memory.

# Python has no command for declaring a variable.
# A variable is created the moment you first assign a value to it.

john_age = 5
john_first_name = 'john'

print(john_age)
print(john_first_name)

# variables do not need to be declared with any particular type,
# and can even change type after they have been set.


# Casting:

stringType = str(3)  # x will be '3'
integerType = int(3)  # y will be 3
floatType = float(3)  # z will be 3.0

print(stringType)
print(integerType)
print(floatType)

# type() function

print(type(stringType))
print(type(integerType))
print(type(floatType))

